typedef struct {
  bool glitch;
  uint8_t frame_count;
  uint16_t timer;
  bool timer_initialized;
  uint16_t last_burst_bucket;
  uint8_t phase_offset;
} glitch_anim_state_t;

static uint8_t glitch_buf[512];
static glitch_anim_state_t text_state = {.glitch = true, .frame_count = 15, .last_burst_bucket = 0xFFFF, .phase_offset = 0};
static glitch_anim_state_t logo_state = {.glitch = true, .frame_count = 15, .last_burst_bucket = 0xFFFF, .phase_offset = 4};

static void render_clean(const char *clean_bitmap) {
  oled_write_raw_P(clean_bitmap, 512);
}

static void apply_row_shift(uint8_t *buf, uint8_t page, int8_t shift) {
  uint8_t row[128];
  uint16_t base = (uint16_t)page * 128;

  for (uint8_t x = 0; x < 128; x++) {
    int16_t src = (int16_t)x - shift;
    while (src < 0) {
      src += 128;
    }
    src %= 128;
    row[x] = buf[base + (uint8_t)src];
  }

  memcpy(&buf[base], row, sizeof(row));
}

static void apply_page_shift(uint8_t *buf, int8_t page_shift) {
  uint8_t copy[512];

  memcpy(copy, buf, sizeof(copy));

  for (uint8_t page = 0; page < 4; page++) {
    int8_t src_page = (int8_t)page - page_shift;
    while (src_page < 0) {
      src_page += 4;
    }
    src_page %= 4;
    memcpy(&buf[(uint16_t)page * 128], &copy[(uint16_t)src_page * 128], 128);
  }
}

static void render_glitch_frame(const char *clean_bitmap, uint8_t phase_offset) {
  static const int8_t shift_seq[8] = {-5, -3, -1, 2, 4, 1, -2, 3};
  uint8_t phase = (uint8_t)((timer_read32() >> 4) + phase_offset);

  memcpy_P(glitch_buf, clean_bitmap, sizeof(glitch_buf));

  for (uint8_t i = 0; i < 4; i++) {
    uint8_t page = (phase + i) & 0x03;
    int8_t shift = shift_seq[(phase + i) & 0x07];
    if (shift != 0) {
      apply_row_shift(glitch_buf, page, shift);
    }
  }

  {
    uint8_t stripe[24];
    uint8_t page = (phase >> 1) & 0x03;
    uint8_t start = (uint8_t)((phase * 7) % 104);
    uint8_t len = 8 + ((phase >> 2) % 16);
    int8_t delta = shift_seq[(phase >> 3) & 0x07];
    uint16_t base = (uint16_t)page * 128 + start;

    if (start + len > 128) {
      len = 128 - start;
    }

    memcpy(stripe, &glitch_buf[base], len);

    uint8_t dst_start = start;
    if (delta > 0) {
      dst_start = (start + (uint8_t)delta + len > 128) ? (128 - len) : (start + (uint8_t)delta);
    } else {
      uint8_t amount = (uint8_t)(-delta);
      dst_start = (start < amount) ? 0 : (start - amount);
    }

    /* Duplicate-stripe echo: shifted copy without erasing source. */
    memcpy(&glitch_buf[(uint16_t)page * 128 + dst_start], stripe, len);
  }

  if ((phase & 0x03) == 0) {
    int8_t vshift = ((phase >> 2) & 0x01) ? 1 : -1;
    apply_page_shift(glitch_buf, vshift);
  }

  if ((phase & 0x07) == 3) {
    uint8_t stripe[20];
    uint8_t src_page = (phase >> 3) & 0x03;
    uint8_t dst_page = (src_page + (((phase >> 2) & 0x01) ? 1 : 3)) & 0x03;
    uint8_t start = (uint8_t)((phase * 11) % 108);
    uint8_t len = 6 + ((phase >> 1) % 12);
    int8_t xoff = (int8_t)((phase & 0x03) - 1);
    uint16_t src_base = (uint16_t)src_page * 128 + start;

    if (start + len > 128) {
      len = 128 - start;
    }

    memcpy(stripe, &glitch_buf[src_base], len);

    uint8_t dst_start = start;
    if (xoff > 0) {
      dst_start = (start + (uint8_t)xoff + len > 128) ? (128 - len) : (start + (uint8_t)xoff);
    } else if (xoff < 0) {
      uint8_t amount = (uint8_t)(-xoff);
      dst_start = (start < amount) ? 0 : (start - amount);
    }

    memcpy(&glitch_buf[(uint16_t)dst_page * 128 + dst_start], stripe, len);
  }

  oled_write_raw((char *)glitch_buf, sizeof(glitch_buf));
}

static void render_glitch_anim(const char *clean_bitmap, glitch_anim_state_t *state) {
  if (!state->timer_initialized) {
    state->timer = timer_read();
    state->timer_initialized = true;
  }

  uint16_t elapsed = timer_elapsed(state->timer);

  if (elapsed > 10000) {
    state->timer = timer_read();
    elapsed = 0;
  }

  if (elapsed < 200 || elapsed > 9700) {
    render_glitch_frame(clean_bitmap, state->phase_offset);
    return;
  }

  if (state->glitch && state->frame_count != 0) {
    state->frame_count--;
    render_glitch_frame(clean_bitmap, state->phase_offset);
    return;
  }

  state->glitch = false;
  render_clean(clean_bitmap);

  {
    uint16_t bucket = elapsed / 700;
    if (bucket != state->last_burst_bucket) {
      state->last_burst_bucket = bucket;
      if ((bucket % 3) == 1) {
        state->glitch = true;
        state->frame_count = 3 + (bucket % 5);
      }
    }
  }
}

void arasaka_render_text_anim(void) {
  render_glitch_anim(text_clean, &text_state);
}

void arasaka_render_logo_anim(void) {
  render_glitch_anim(epd_bitmap_Arasaka_logo, &logo_state);
}
