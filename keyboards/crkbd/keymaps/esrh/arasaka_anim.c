static bool arasaka_glitch = true;
static uint8_t arasaka_frame_count = 15;
static uint16_t arasaka_timer = 0;
static bool arasaka_timer_initialized = false;

static void arasaka_text_clean(void) {
  oled_write_raw_P(text_clean, 512);
}

static void arasaka_text_glitch(void) {
  uint8_t frame = rand() % 7;
  oled_write_raw_P(text_glitch[frame], 512);
}

void arasaka_render_text_anim(void) {
  if (!arasaka_timer_initialized) {
    arasaka_timer = timer_read();
    arasaka_timer_initialized = true;
  }

  uint16_t elapsed = timer_elapsed(arasaka_timer);

  if (elapsed < 150) {
    arasaka_text_glitch();
    return;
  }

  if (elapsed < 250) {
    arasaka_text_glitch();
    return;
  }

  if (elapsed > 9750 && elapsed < 9850) {
    arasaka_text_glitch();
    return;
  }

  if (elapsed > 9850 && elapsed < 10000) {
    arasaka_text_glitch();
    return;
  }

  if (elapsed > 10000) {
    arasaka_timer = timer_read();
  }

  if (arasaka_glitch && arasaka_frame_count != 0) {
    arasaka_frame_count--;
    arasaka_text_glitch();
    return;
  }

  arasaka_glitch = false;
  arasaka_text_clean();

  if (rand() % 50 == 1) {
    arasaka_glitch = true;
    arasaka_frame_count = 2 + rand() % 10;
  }
}
